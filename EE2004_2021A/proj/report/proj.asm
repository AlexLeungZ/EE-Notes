;(User_edit)Button 8_8's setting - goto exit
Button_P8_braEXIT:
    call Message_EXITING_setting
    call Message_movable
    call Message_null_setting
    call Message_fbf
    bra Exit
return

;()Delay time between button's signal
Delay_input:
    movlw 0x28
    movwf BUTTON_DELAY_REG
    Delay_input_2:
        decf BUTTON_DELAY_REG, f
        bnz Delay_input_2
return

;()Display right to left moving message
Message_movable:
    incf MESSAGE_LENGTH_REG_L, f
    movlw 0x00
    addwfc MESSAGE_LENGTH_REG_H
    Message_movable_not_FF:
        call Message_display_repeat
        incf TABLE_ini_pos_L, f
        movlw 0x00
        addwfc TABLE_ini_pos_H
        decf MESSAGE_LENGTH_REG_L, f
        bnz Message_movable_not_FF
    tstfsz MESSAGE_LENGTH_REG_H
    call Message_movable_inner
return

;()Display right to left moving message - inner loop 256 times (if length > 255)
Message_movable_inner:
    clrf MESSAGE_LENGTH_REG_FF
    Message_movable_FF:
        call Message_display_repeat
        incf TABLE_ini_pos_L, f
        movlw 0x00
        addwfc TABLE_ini_pos_H
        decf MESSAGE_LENGTH_REG_FF, f
        bnz Message_movable_FF
    decf MESSAGE_LENGTH_REG_H, f
    bnz Message_movable_inner
return

;()Display animation message
Message_fbf:
    call Message_display_repeat
    movlw 0x10
    addwf TABLE_ini_pos_L
    movlw 0x00
    addwfc TABLE_ini_pos_H
    decf MESSAGE_LENGTH_REG_L, f
    bnz Message_fbf
return

;()Display loading message - hourglass (default: display 2 times with speed 5)
Message_loading_hourglass:
    movff MESSGAE_SPEED_REG, MESSAGE_GOLBAL_SPEED_VAR
    movlw d'5'
    movwf MESSGAE_SPEED_REG
    movlw d'2'
    movwf MESSAGE_special_loop_VAR
    Message_loading_hourglass_inner:
        call Message_fbf_hourglass_setting
        call Message_fbf
        decf MESSAGE_special_loop_VAR
        bnz Message_loading_hourglass_inner
    movff MESSAGE_GOLBAL_SPEED_VAR, MESSGAE_SPEED_REG
return

;()Display loading message arrow (default: display 1 time with speed 5)
Message_loading_arrow:
    movff MESSGAE_SPEED_REG, MESSAGE_GOLBAL_SPEED_VAR
    movlw d'5'
    movwf MESSGAE_SPEED_REG
    call Message_fbf_arrow_setting
    call Message_fbf
    movff MESSAGE_GOLBAL_SPEED_VAR, MESSGAE_SPEED_REG
return

;()Delay time between LED's on-off (default: set to 0x40)
Delay_output:		
    movlw 0x40
    movwf DELAY_IO_VAR
    Delay_output_2:	
        decf DELAY_IO_VAR, f
        bnz Delay_output_2
return

;()Display message repeatly with n times to show up
Message_display_repeat:
    movff MESSGAE_SPEED_REG, DISPLAY_HOLD_VAR
    Message_display_repeat_2:	
        clrf TABLE_counter_L
        clrf TABLE_counter_H
        movff TABLE_ini_pos_L, TABLE_counter_L
        movff TABLE_ini_pos_H, TABLE_counter_H
        movlw 0x10 
        movwf PORTA_ini_pos_REG
        call Message_display_inner_loop
        movlw 0x08
        movwf PORTA_ini_pos_REG
        call Message_display_inner_loop
        decf DISPLAY_HOLD_VAR, f
        bnz Message_display_repeat_2
return

;()Display message repeatly function - inner loop
Message_display_inner_loop:
    movlw 0x08
    movwf LED_inner_loop_VAR
    Message_display_inner_loop_1:
        movff PORTA_ini_pos_REG, PORTA
        clrf PORTC
        clrf PORTD
        call look_up_table_C
        movwf PORTC
        call look_up_table_D
        movwf PORTD
        incf TABLE_counter_L, f
        movlw 0x00
        addwfc TABLE_counter_H
        incf PORTA_ini_pos_REG ,f
        call Delay_output
        decf LED_inner_loop_VAR, f
        bnz Message_display_inner_loop_1
return

;()Look-up table function for message upper part
look_up_table_C:  
    movff TABLE_location_C_L, TBLPTRL
    movff TABLE_location_C_H, TBLPTRH
    movff TABLE_location_C_U, TBLPTRU
    movf TABLE_counter_L, w
    addwf TBLPTRL
    movf TABLE_counter_H, w
    addwfc TBLPTRH
    movlw 0x00
    addwfc TBLPTRU
    TBLRD*
    movf TABLAT, w
return

;()Look-up table function for message lower part
look_up_table_D:  
    movff TABLE_location_D_L, TBLPTRL
    movff TABLE_location_D_H, TBLPTRH
    movff TABLE_location_D_U, TBLPTRU
    movf TABLE_counter_L, w
    addwf TBLPTRL
    movf TABLE_counter_H, w
    addwfc TBLPTRH
    movlw 0x00
    addwfc TBLPTRU
    TBLRD*
    movf TABLAT, w
return

