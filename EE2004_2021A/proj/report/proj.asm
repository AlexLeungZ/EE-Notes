LIST P=18F4520 ;directive to define processor
#include <P18F4520.INC> ;CPU specific variable definitions

;----------------------------------------------------------------
;Button list:
;0) Start:              show message "HELLO!"
    ;1) Button 1:       show a message (chinese 1) + (show hourglass)
    ;2) Button 2:       show a message (chinese 2) + (show hourglass)
    ;3) Button 3:       show a message (name & SID) + (show hourglass)
    ;4) Button 4:       show a animation + (show hourglass)
    ;5) Button 5:       show a message (testing) + (show hourglass)
    ;6) Button 6:       set display speed (fast mode) + (show hourglass)
    ;7) Button 7:       set display speed (slow mode) + (show hourglass)
    ;8) Button 8:       (show hourglass) then
    ;   Button8_5:      goto Start
    ;   Button8_8:      show message "EXITING!" + turn off LED + goto Exit
;----------------------------------------------------------------

;Configuration bits definitions
CONFIG OSC = XT
CONFIG WDT = OFF
CONFIG LVP = OFF

;Variable definitions and settings
ORG 0x0000
CBLOCK 0x0010
    BUTTON_DELAY_REG
    MESSAGE_LENGTH_REG_L
    MESSAGE_LENGTH_REG_H
    MESSAGE_LENGTH_REG_FF
    DELAY_IO_VAR
    DISPLAY_HOLD_VAR
    MESSGAE_SPEED_REG
    MESSAGE_GOLBAL_SPEED_VAR
    MESSAGE_special_loop_VAR
    TABLE_location_C_L
    TABLE_location_C_H
    TABLE_location_C_U
    TABLE_location_D_L
    TABLE_location_D_H
    TABLE_location_D_U
    TABLE_counter_L
    TABLE_counter_H
    TABLE_ini_pos_L
    TABLE_ini_pos_H
    LED_inner_loop_VAR
    PORTA_ini_pos_REG
ENDC

;IO settings
movlw 0x0f
movwf ADCON1
setf TRISB                          ;Set Port B direction input
clrf TRISA                          ;Set Port A direction output (3-8 decoder)
clrf TRISC                          ;Set Port C direction output (m1 or m2)
clrf TRISD                          ;Set Port D direction output (m3 or m4)

;Reset vector
goto Start
ORG 0x0080

;Start of main program
Start:
    movlw d'10'                      ;default speed of displaying message: fast mode
    movwf MESSGAE_SPEED_REG
    call Message_HELLO_setting
    call Message_movable

MainLoop: 	
    call Message_loading_arrow
    btfss PORTB, 0
    call Button_P1_function
    btfss PORTB, 1
    call Button_P2_function
    btfss PORTB, 2
    call Button_P3_function
    btfss PORTB, 3
    call Button_P4_function
    btfss PORTB, 4
    call Button_P5_function
    btfss PORTB, 5
    call Button_P6_function
    btfss PORTB, 6
    call Button_P7_function
    btfss PORTB, 7
    call Button_P8_function
goto MainLoop

;Exits the program
Exit: goto Exit 

;*(User_edit)Button 1's setting - set to display message - CHINESE message
Button_P1_function:
    call Message_CHINESE_setting
    call Message_movable
    call Message_loading_hourglass
    wait_up_P1:
        btfss PORTB, 0
        bra wait_up_P1
    call Delay_input
return

